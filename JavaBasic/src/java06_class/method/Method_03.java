package java06_class.method;

public class Method_03 {
	public void printI() {
		for (int i = 0;i<100;i++) {
			System.out.println(i+1);
		}
		
	}
	public void printH(int n) {
		for (int i = 0;i<n;i++) {
			System.out.println("hello");
		}
	}
	
	public void printS(int n,String str) {
		for (int i = 0;i<n;i++) {
			System.out.println(str);
		}
	}

}
